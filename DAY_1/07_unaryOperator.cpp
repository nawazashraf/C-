#include<iostream>
using namespace std;

int main(){
    //Operators that only need one Value/Operand are called Unary Operator

    int a = 5;
    cout << -a << endl;  // -5
    cout << +a << endl;  // 5

    //a++ = a= a+1
    a++;  // PostIncrement = a is used first and then incremented from next line
    cout << a << endl; // 6
    
    //a-- PreIncrement = a is incremented first

 
    //a-- = a= a-1


}